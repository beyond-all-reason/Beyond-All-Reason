-- ============================================================================
-- 3D Translate Gizmo
-- ============================================================================
-- For each editable point (pos for point/cone/beam, pos2 for beam, plus a
-- "both" center handle for beam), we draw three colored axis-arrows plus a
-- small center cube. The user can click+drag any axis to move that point
-- along that axis only (Blender/Maya/3dsMax style translate gizmo).
--
-- Geometry: arrows live in *unit world space* — they always point along the
-- unit's heading-rotated X (red), Y (green), Z (blue). That matches the
-- light's piece-local coordinate system after the unit world transform.
--
-- Hit-testing: each axis is projected to screen space (start + end), and
-- we compute the perpendicular distance from the cursor to that 2D segment.
-- The closest axis within HOVER_PIXELS is "hovered". A center cube at the
-- gizmo origin is hit-tested as a small circle in screen space.
--
-- Dragging: mouse-delta is projected onto the axis's screen-space direction,
-- divided by the screen-space pixels-per-world-unit factor for that axis,
-- then converted from world delta back to piece-local delta via the inverse
-- rotation we already maintain.

local GIZMO_AXIS_LEN = 9            -- world elmos
local GIZMO_HEAD_LEN = 2            -- arrowhead length
local GIZMO_HEAD_RADIUS = 1         -- arrowhead "fatness"
local GIZMO_CENTER_SIZE = 2.5       -- center cube half-size (world)
local GIZMO_PLANE_OFFSET = 3        -- where the plane quad starts along each axis
local GIZMO_PLANE_SIZE = 2.5        -- plane quad side length (world)
local HOVER_PIXELS = 10             -- how close cursor must be to an axis (px)
local CENTER_HOVER_PIXELS = 18      -- "both" center handle gets a fatter hit zone

local AXIS_COLORS = {
	x = {0.95, 0.25, 0.25},
	y = {0.25, 0.95, 0.30},
	z = {0.25, 0.45, 0.95},
	all = {0.95, 0.85, 0.30},
	xy = {0.85, 0.85, 0.30},  -- red+green = yellow
	yz = {0.30, 0.85, 0.85},  -- green+blue = cyan
	xz = {0.85, 0.30, 0.85},  -- red+blue = magenta
}

-- For each plane, the two local axes that define it.
local PLANE_AXES = {
	xy = {"x", "y"},
	yz = {"y", "z"},
	xz = {"x", "z"},
}

local function colMul(c, mul)
	return {c[1]*mul, c[2]*mul, c[3]*mul}
end

-- Returns the world-space vectors corresponding to piece-local +X, +Y, +Z
-- (each one a unit vector). Used to draw the axes and to project deltas.
local function getAxisWorldDirs()
	local rx1, ry1, rz1 = pieceLocalDirToWorld(1, 0, 0)
	local rx2, ry2, rz2 = pieceLocalDirToWorld(0, 1, 0)
	local rx3, ry3, rz3 = pieceLocalDirToWorld(0, 0, 1)
	return {x={rx1,ry1,rz1}, y={rx2,ry2,rz2}, z={rx3,ry3,rz3}}
end

-- Returns world-space position of a "point" identified by name: "pos" or "pos2".
local function getPointWorld(which)
	local v = state.values
	if which == "pos" then
		return pieceLocalToWorld(v.posx or 0, v.posy or 0, v.posz or 0)
	elseif which == "pos2" then
		return pieceLocalToWorld(v.pos2x or 0, v.pos2y or 0, v.pos2z or 0)
	end
end

-- Distance (squared) from point P to a 2D segment AB.
local function distSqPointToSegment(px, py, ax, ay, bx, by)
	local dx, dy = bx - ax, by - ay
	local len2 = dx*dx + dy*dy
	if len2 < 1e-6 then
		local ex, ey = px - ax, py - ay
		return ex*ex + ey*ey
	end
	local t = ((px - ax) * dx + (py - ay) * dy) / len2
	if t < 0 then t = 0 elseif t > 1 then t = 1 end
	local cx, cy = ax + t*dx, ay + t*dy
	local ex, ey = px - cx, py - cy
	return ex*ex + ey*ey
end

-- Project a world point to screen. Returns sx, sy, depth (or nil if behind camera).
local function worldToScreen(x, y, z)
	local sx, sy, sz = spWorldToScreenCoords(x, y, z)
	return sx, sy, sz
end

-- For a given gizmo origin (world), compute screen positions of the 3 axis tips
-- plus the origin itself. Returns table or nil if origin is off-screen.
local function getGizmoScreenLayout(originX, originY, originZ)
	local sx, sy = worldToScreen(originX, originY, originZ)
	if not sx then return nil end
	local axes = getAxisWorldDirs()
	local len = GIZMO_AXIS_LEN
	local function tip(ax)
		local d = axes[ax]
		local tx = originX + d[1] * len
		local ty = originY + d[2] * len
		local tz = originZ + d[3] * len
		local tsx, tsy = worldToScreen(tx, ty, tz)
		if not tsx then return nil end
		return {sx=tsx, sy=tsy, wx=tx, wy=ty, wz=tz}
	end
	return {
		origin = {sx=sx, sy=sy, wx=originX, wy=originY, wz=originZ},
		x = tip("x"),
		y = tip("y"),
		z = tip("z"),
		axes = axes,
	}
end

-- Hover detection. Returns {point="pos"|"pos2", axis="x"|"y"|"z"|"all"} or nil.
--
-- Resolution rules:
--   1. The "both" center handle (beam only) wins if cursor is within
--      CENTER_HOVER_PIXELS of its origin -- it's a small target sandwiched
--      between two endpoint gizmos and would otherwise lose to nearby axes.
--   2. Otherwise we pick the closest axis segment within HOVER_PIXELS,
--      considering all gizmo points (pos, pos2).

-- Forward declarations: hitTestGizmo uses these helpers, but they are
-- defined later in the file because drawPlane consumes them too.
local getPlaneQuadCorners, pointInQuad, getMultiCentroid

local function hitTestGizmo(mx, my)
	if not state.currentUnitID then return nil end

	-- Multi-mode: only the centroid gizmo is hit-testable.
	if isMultiMode() then
		local cx, cy, cz = getMultiCentroid()
		if not cx then return nil end
		-- Plane handles first
		for _, plane in ipairs({"xy", "yz", "xz"}) do
			local corners = getPlaneQuadCorners(cx, cy, cz, plane)
			local quadScreen = {}
			local ok = true
			for i = 1, 4 do
				local sx, sy = spWorldToScreenCoords(corners[i][1], corners[i][2], corners[i][3])
				if not sx then ok = false; break end
				quadScreen[i] = {sx, sy}
			end
			if ok and pointInQuad(mx, my, quadScreen) then
				return {point="multi", axis=plane}
			end
		end
		-- Then axes
		local layout = getGizmoScreenLayout(cx, cy, cz)
		if layout then
			local best, bestScore = nil, HOVER_PIXELS * HOVER_PIXELS
			for _, ax in ipairs({"x","y","z"}) do
				local tip = layout[ax]
				if tip then
					local d = distSqPointToSegment(mx, my,
						layout.origin.sx, layout.origin.sy, tip.sx, tip.sy)
					if d < bestScore then
						best = {point="multi", axis=ax}
						bestScore = d
					end
				end
			end
			return best
		end
		return nil
	end

	if state.lightType ~= "point" and state.lightType ~= "cone" and state.lightType ~= "beam" then
		return nil
	end

	local p1x, p1y, p1z = getPointWorld("pos")
	local p2x, p2y, p2z
	local mxw, myw, mzw
	if state.lightType == "beam" then
		p2x, p2y, p2z = getPointWorld("pos2")
		if p1x and p2x then
			mxw = (p1x + p2x) * 0.5
			myw = (p1y + p2y) * 0.5
			mzw = (p1z + p2z) * 0.5
		end
	end

	-- Rule 1: plane quads on the "both" gizmo only.
	if mxw then
		for _, plane in ipairs({"xy", "yz", "xz"}) do
			local corners = getPlaneQuadCorners(mxw, myw, mzw, plane)
			local quadScreen = {}
			local ok = true
			for i = 1, 4 do
				local sx, sy = spWorldToScreenCoords(corners[i][1], corners[i][2], corners[i][3])
				if not sx then ok = false; break end
				quadScreen[i] = {sx, sy}
			end
			if ok and pointInQuad(mx, my, quadScreen) then
				return {point="both", axis=plane}
			end
		end
	end

	-- Rule 3: closest axis among all gizmos (endpoints + "both").
	local best = nil
	local bestScore = HOVER_PIXELS * HOVER_PIXELS
	local function testGizmoAxes(which, x, y, z)
		if not x then return end
		local layout = getGizmoScreenLayout(x, y, z)
		if not layout then return end
		for _, ax in ipairs({"x","y","z"}) do
			local tip = layout[ax]
			if tip then
				local d = distSqPointToSegment(mx, my,
					layout.origin.sx, layout.origin.sy,
					tip.sx, tip.sy)
				if d < bestScore then
					best = {point=which, axis=ax}
					bestScore = d
				end
			end
		end
	end
	testGizmoAxes("pos", p1x, p1y, p1z)
	testGizmoAxes("pos2", p2x, p2y, p2z)
	if mxw then testGizmoAxes("both", mxw, myw, mzw) end
	return best
end

-- Draw a single axis arrow from origin in world direction d (unit vector), length L.
local function drawAxis(ox, oy, oz, dirx, diry, dirz, color, isHovered)
	local tipx = ox + dirx * GIZMO_AXIS_LEN
	local tipy = oy + diry * GIZMO_AXIS_LEN
	local tipz = oz + dirz * GIZMO_AXIS_LEN
	local mul = isHovered and 1.2 or 1.0
	local c = color
	gl.Color(math.min(1, c[1]*mul), math.min(1, c[2]*mul), math.min(1, c[3]*mul), 1)
	gl.LineWidth(isHovered and 4 or 2)
	gl.BeginEnd(GL.LINES, function()
		gl.Vertex(ox, oy, oz)
		gl.Vertex(tipx, tipy, tipz)
	end)
	-- Arrowhead: small cross at tip perpendicular to axis (cheap; not pretty but readable).
	local hsize = GIZMO_HEAD_RADIUS * (isHovered and 1.4 or 1.0)
	gl.BeginEnd(GL.LINES, function()
		gl.Vertex(tipx - hsize, tipy, tipz); gl.Vertex(tipx + hsize, tipy, tipz)
		gl.Vertex(tipx, tipy - hsize, tipz); gl.Vertex(tipx, tipy + hsize, tipz)
		gl.Vertex(tipx, tipy, tipz - hsize); gl.Vertex(tipx, tipy, tipz + hsize)
	end)
end

-- Returns world-space corners of a plane quad sitting between the two axes
-- of `planeName` ("xy", "yz", "xz"), starting at GIZMO_PLANE_OFFSET from
-- origin and extending GIZMO_PLANE_SIZE further. Quad lies in the plane
-- spanned by those two world-direction axes, with origin at the unit's gizmo.
function getPlaneQuadCorners(ox, oy, oz, planeName)
	local axes = getAxisWorldDirs()
	local a, b = PLANE_AXES[planeName][1], PLANE_AXES[planeName][2]
	local da, db = axes[a], axes[b]
	local off = GIZMO_PLANE_OFFSET
	local sz = GIZMO_PLANE_SIZE
	-- Four corners along the two-axis grid: (off,off), (off+sz,off),
	-- (off+sz,off+sz), (off,off+sz). Each corner returned as {x,y,z}.
	local function pt(ta, tb)
		return {
			ox + da[1]*ta + db[1]*tb,
			oy + da[2]*ta + db[2]*tb,
			oz + da[3]*ta + db[3]*tb,
		}
	end
	return {
		pt(off,    off),
		pt(off+sz, off),
		pt(off+sz, off+sz),
		pt(off,    off+sz),
	}
end

-- 2D point-in-quad test (winding number). Quad given as 4 {sx,sy} pairs.
function pointInQuad(px, py, q)
	local function side(ax, ay, bx, by, cx, cy)
		return (bx - ax) * (cy - ay) - (by - ay) * (cx - ax)
	end
	local s1 = side(q[1][1], q[1][2], q[2][1], q[2][2], px, py)
	local s2 = side(q[2][1], q[2][2], q[3][1], q[3][2], px, py)
	local s3 = side(q[3][1], q[3][2], q[4][1], q[4][2], px, py)
	local s4 = side(q[4][1], q[4][2], q[1][1], q[1][2], px, py)
	-- All same sign (positive or negative) means inside (assuming convex quad).
	return (s1 >= 0 and s2 >= 0 and s3 >= 0 and s4 >= 0)
	    or (s1 <= 0 and s2 <= 0 and s3 <= 0 and s4 <= 0)
end

local function drawPlane(ox, oy, oz, planeName, isHovered)
	local corners = getPlaneQuadCorners(ox, oy, oz, planeName)
	local color = AXIS_COLORS[planeName]
	local fillAlpha = isHovered and 0.55 or 0.25
	local mul = isHovered and 1.2 or 1.0
	-- Fill
	gl.Color(math.min(1, color[1]*mul), math.min(1, color[2]*mul), math.min(1, color[3]*mul), fillAlpha)
	gl.BeginEnd(GL.QUADS, function()
		for i = 1, 4 do gl.Vertex(corners[i][1], corners[i][2], corners[i][3]) end
	end)
	-- Outline
	gl.Color(math.min(1, color[1]*mul), math.min(1, color[2]*mul), math.min(1, color[3]*mul), 1)
	gl.LineWidth(isHovered and 3 or 2)
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 1, 4 do gl.Vertex(corners[i][1], corners[i][2], corners[i][3]) end
	end)
end

local function drawCenterHandle(ox, oy, oz, color, isHovered)
	local s = GIZMO_CENTER_SIZE * (isHovered and 1.4 or 1.0)
	local mul = isHovered and 1.3 or 1.0
	gl.Color(math.min(1, color[1]*mul), math.min(1, color[2]*mul), math.min(1, color[3]*mul), 1)
	gl.LineWidth(isHovered and 4 or 3)
	-- 3 perpendicular squares
	gl.BeginEnd(GL.LINE_LOOP, function()
		gl.Vertex(ox - s, oy - s, oz); gl.Vertex(ox + s, oy - s, oz)
		gl.Vertex(ox + s, oy + s, oz); gl.Vertex(ox - s, oy + s, oz)
	end)
	gl.BeginEnd(GL.LINE_LOOP, function()
		gl.Vertex(ox - s, oy, oz - s); gl.Vertex(ox + s, oy, oz - s)
		gl.Vertex(ox + s, oy, oz + s); gl.Vertex(ox - s, oy, oz + s)
	end)
	gl.BeginEnd(GL.LINE_LOOP, function()
		gl.Vertex(ox, oy - s, oz - s); gl.Vertex(ox, oy + s, oz - s)
		gl.Vertex(ox, oy + s, oz + s); gl.Vertex(ox, oy - s, oz + s)
	end)
end

local function drawGizmoAt(originX, originY, originZ, which)
	local hov = state.gizmo
	local activeMatch = hov and hov.point == which
	if which == "multi" then
		-- Same look as "both": axes + plane handles, no center cube.
		local axes = getAxisWorldDirs()
		for _, ax in ipairs({"x","y","z"}) do
			local d = axes[ax]
			local hovered = activeMatch and hov.axis == ax
			drawAxis(originX, originY, originZ, d[1], d[2], d[3], AXIS_COLORS[ax], hovered)
		end
		for _, plane in ipairs({"xy", "yz", "xz"}) do
			local hovered = activeMatch and hov.axis == plane
			drawPlane(originX, originY, originZ, plane, hovered)
		end
		return
	end
	if which == "both" then
		-- "Both" gizmo: 3 axes + 3 plane quads. Center "free 3D" cube removed
		-- per user request.
		local axes = getAxisWorldDirs()
		for _, ax in ipairs({"x","y","z"}) do
			local d = axes[ax]
			local hovered = activeMatch and hov.axis == ax
			drawAxis(originX, originY, originZ, d[1], d[2], d[3], AXIS_COLORS[ax], hovered)
		end
		for _, plane in ipairs({"xy", "yz", "xz"}) do
			local hovered = activeMatch and hov.axis == plane
			drawPlane(originX, originY, originZ, plane, hovered)
		end
		return
	end
	-- Endpoint gizmos: just the three axis arrows.
	local axes = getAxisWorldDirs()
	for _, ax in ipairs({"x","y","z"}) do
		local d = axes[ax]
		local hovered = activeMatch and hov.axis == ax
		drawAxis(originX, originY, originZ, d[1], d[2], d[3], AXIS_COLORS[ax], hovered)
	end
end

-- Draws three thin great-circles (XY, YZ, XZ planes) of a sphere centered at
-- (cx,cy,cz). Used to visualize point-light radius. World-axis-aligned: cheap
-- and good enough as a radius indicator.
local function drawWireSphere(cx, cy, cz, radius, segments)
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 0, segments - 1 do
			local a = (i / segments) * math.pi * 2
			gl.Vertex(cx + math.cos(a)*radius, cy + math.sin(a)*radius, cz)
		end
	end)
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 0, segments - 1 do
			local a = (i / segments) * math.pi * 2
			gl.Vertex(cx, cy + math.sin(a)*radius, cz + math.cos(a)*radius)
		end
	end)
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 0, segments - 1 do
			local a = (i / segments) * math.pi * 2
			gl.Vertex(cx + math.cos(a)*radius, cy, cz + math.sin(a)*radius)
		end
	end)
end

-- Wireframe capsule from (ax,ay,az) to (bx,by,bz) with given radius. Drawn as:
-- two end circles perpendicular to the axis, N "rib" lines connecting them
-- along the cylinder body, and a small dome of cross-circles at each end so
-- the rounded caps read as spheres.
local function drawCapsule(ax, ay, az, bx, by, bz, radius, segments)
	local axisX, axisY, axisZ = bx - ax, by - ay, bz - az
	local axisLen = math.sqrt(axisX*axisX + axisY*axisY + axisZ*axisZ)
	if axisLen < 1e-4 then
		-- Degenerate: just draw a sphere at one end.
		drawWireSphere(ax, ay, az, radius, segments)
		return
	end
	local nx, ny, nz = axisX/axisLen, axisY/axisLen, axisZ/axisLen

	-- Two perpendicular vectors in the plane normal to (nx,ny,nz).
	local ux, uy, uz
	if math.abs(ny) < 0.9 then
		-- cross with world up (0,1,0)
		ux, uy, uz = nz, 0, -nx
	else
		ux, uy, uz = 0, nz, -ny
	end
	local ulen = math.sqrt(ux*ux + uy*uy + uz*uz)
	ux, uy, uz = ux/ulen, uy/ulen, uz/ulen
	-- v = n × u
	local vx = ny*uz - nz*uy
	local vy = nz*ux - nx*uz
	local vz = nx*uy - ny*ux

	-- Pre-compute ring offset table (radius * (cos*u + sin*v))
	local function ringPoint(angle)
		local cs = math.cos(angle) * radius
		local sn = math.sin(angle) * radius
		return ux*cs + vx*sn, uy*cs + vy*sn, uz*cs + vz*sn
	end

	-- 1. End ring at A
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 0, segments - 1 do
			local ox, oy, oz = ringPoint((i / segments) * math.pi * 2)
			gl.Vertex(ax + ox, ay + oy, az + oz)
		end
	end)
	-- 2. End ring at B
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 0, segments - 1 do
			local ox, oy, oz = ringPoint((i / segments) * math.pi * 2)
			gl.Vertex(bx + ox, by + oy, bz + oz)
		end
	end)
	-- 3. Connecting ribs along the cylinder body (every Nth segment)
	local ribCount = math.min(segments, 8)
	gl.BeginEnd(GL.LINES, function()
		for i = 0, ribCount - 1 do
			local angle = (i / ribCount) * math.pi * 2
			local ox, oy, oz = ringPoint(angle)
			gl.Vertex(ax + ox, ay + oy, az + oz)
			gl.Vertex(bx + ox, by + oy, bz + oz)
		end
	end)
	-- 4. End-cap domes: one half-circle in the plane spanned by axis & u,
	--    another in the plane spanned by axis & v. Drawn at each endpoint,
	--    extending OUTWARD past the cylinder cap.
	local function drawDome(cx, cy, cz, sign)
		-- Half circle in (u, n) plane
		gl.BeginEnd(GL.LINE_STRIP, function()
			for i = 0, segments do
				local a = (i / segments) * math.pi
				local cs = math.cos(a) * radius
				local sn = math.sin(a) * radius * sign
				gl.Vertex(cx + ux*cs + nx*sn, cy + uy*cs + ny*sn, cz + uz*cs + nz*sn)
			end
		end)
		-- Half circle in (v, n) plane
		gl.BeginEnd(GL.LINE_STRIP, function()
			for i = 0, segments do
				local a = (i / segments) * math.pi
				local cs = math.cos(a) * radius
				local sn = math.sin(a) * radius * sign
				gl.Vertex(cx + vx*cs + nx*sn, cy + vy*cs + ny*sn, cz + vz*cs + nz*sn)
			end
		end)
	end
	drawDome(ax, ay, az, -1)  -- A side, dome extends in -axis direction
	drawDome(bx, by, bz,  1)  -- B side, dome extends in +axis direction
end

-- Draws a circle in world space lying in the plane perpendicular to (dx,dy,dz),
-- centered at (cx,cy,cz), with the given radius. (dx,dy,dz) is the plane normal.
local function drawCircleNormal(cx, cy, cz, dx, dy, dz, radius, segments)
	local len = math.sqrt(dx*dx + dy*dy + dz*dz)
	if len < 1e-6 then return end
	dx, dy, dz = dx/len, dy/len, dz/len
	-- Build two perpendicular vectors in the plane
	local ux, uy, uz
	if math.abs(dy) < 0.9 then
		-- cross with world up
		ux, uy, uz = dz, 0, -dx
	else
		ux, uy, uz = 0, dz, -dy
	end
	local ulen = math.sqrt(ux*ux + uy*uy + uz*uz)
	ux, uy, uz = ux/ulen, uy/ulen, uz/ulen
	-- v = d × u
	local vx = dy*uz - dz*uy
	local vy = dz*ux - dx*uz
	local vz = dx*uy - dy*ux
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 0, segments - 1 do
			local a = (i / segments) * math.pi * 2
			local cs = math.cos(a) * radius
			local sn = math.sin(a) * radius
			gl.Vertex(cx + ux*cs + vx*sn, cy + uy*cs + vy*sn, cz + uz*cs + vz*sn)
		end
	end)
end

local function drawLightMarker()
	local x, y, z = getLightPosWorld()
	if not x then return end
	local v = state.values
	local r = math.max(0.3, math.min(1, v.r or 1))
	local g = math.max(0.3, math.min(1, v.g or 1))
	local b = math.max(0.3, math.min(1, v.b or 1))
	local size = math.max(3, math.min(10, (v.radius or 0) * 0.1))

	gl.PushAttrib(GL.LINE_BIT)
	gl.DepthTest(false)
	gl.LineWidth(2)
	gl.Color(r, g, b, 1)

	local radius = v.radius or 0

	if state.lightType == "point" then
		drawCross(x, y, z, size)
		-- Radius wireframe sphere
		if radius > 1 then
			gl.LineWidth(1)
			gl.Color(r, g, b, 0.35)
			drawWireSphere(x, y, z, radius, 32)
			gl.LineWidth(2)
			gl.Color(r, g, b, 1)
		end
	elseif state.lightType == "cone" then
		drawCross(x, y, z, size)
		local lx = v.dirx or 0
		local ly = v.diry or 0
		local lz = v.dirz or 1
		local llen = math.sqrt(lx*lx + ly*ly + lz*lz)
		if llen > 1e-4 then
			lx, ly, lz = lx/llen, ly/llen, lz/llen
			local dx, dy, dz = pieceLocalDirToWorld(lx, ly, lz)
			local arrowLen = (radius > 1) and radius or 30
			local tipx = x + dx * arrowLen
			local tipy = y + dy * arrowLen
			local tipz = z + dz * arrowLen
			gl.BeginEnd(GL.LINES, function()
				gl.Vertex(x, y, z)
				gl.Vertex(tipx, tipy, tipz)
			end)
			gl.LineWidth(2)
			drawCross(tipx, tipy, tipz, size * 0.7)

			-- Cone radius visualization: a ring at the tip whose half-angle
			-- equals theta (cone half-angle). Approximates the lit area.
			if radius > 1 then
				local theta = v.theta or 0.5
				-- ring radius = arrowLen * tan(theta), capped to avoid runaway
				local ringR = arrowLen * math.min(2, math.tan(theta))
				gl.LineWidth(1)
				gl.Color(r, g, b, 0.35)
				drawCircleNormal(tipx, tipy, tipz, dx, dy, dz, ringR, 32)
				-- A few "spokes" from origin to the ring edge to show the cone shape
				gl.BeginEnd(GL.LINES, function()
					for i = 0, 7 do
						local a = (i / 8) * math.pi * 2
						-- We need a point on the ring; use the same basis as drawCircleNormal
						local ndx, ndy, ndz = dx, dy, dz
						local ux, uy, uz
						if math.abs(ndy) < 0.9 then
							ux, uy, uz = ndz, 0, -ndx
						else
							ux, uy, uz = 0, ndz, -ndy
						end
						local ulen = math.sqrt(ux*ux + uy*uy + uz*uz)
						ux, uy, uz = ux/ulen, uy/ulen, uz/ulen
						local vx = ndy*uz - ndz*uy
						local vy = ndz*ux - ndx*uz
						local vz = ndx*uy - ndy*ux
						local cs = math.cos(a) * ringR
						local sn = math.sin(a) * ringR
						gl.Vertex(x, y, z)
						gl.Vertex(tipx + ux*cs + vx*sn, tipy + uy*cs + vy*sn, tipz + uz*cs + vz*sn)
					end
				end)
				gl.LineWidth(2)
				gl.Color(r, g, b, 1)
			end
		end
	elseif state.lightType == "beam" then
		drawCross(x, y, z, size)
		local x2, y2, z2 = pieceLocalToWorld(v.pos2x or 0, v.pos2y or 0, v.pos2z or 0)
		if x2 then
			drawCross(x2, y2, z2, size)
			gl.BeginEnd(GL.LINES, function()
				gl.Vertex(x, y, z)
				gl.Vertex(x2, y2, z2)
			end)
			-- Radius capsule: real cylinder with rounded end-caps
			if radius > 1 then
				gl.LineWidth(1)
				gl.Color(r, g, b, 0.35)
				drawCapsule(x, y, z, x2, y2, z2, radius, 24)
				gl.LineWidth(2)
				gl.Color(r, g, b, 1)
			end
		end
	end

	-- 3D translate gizmos on top of the markers (single-edit mode only)
	if not isMultiMode() then
	if state.lightType == "point" or state.lightType == "cone" then
		drawGizmoAt(x, y, z, "pos")
	elseif state.lightType == "beam" then
		drawGizmoAt(x, y, z, "pos")
		local x2, y2, z2 = pieceLocalToWorld(v.pos2x or 0, v.pos2y or 0, v.pos2z or 0)
		if x2 then
			drawGizmoAt(x2, y2, z2, "pos2")
			-- Center "both" handle at midpoint
			local mxw = (x + x2) * 0.5
			local myw = (y + y2) * 0.5
			local mzw = (z + z2) * 0.5
			drawGizmoAt(mxw, myw, mzw, "both")
		end
	end
	end -- if not isMultiMode

	gl.DepthTest(true)
	gl.Color(1, 1, 1, 1)
	gl.PopAttrib()
end

-- ============================================================================
-- Per-light overview markers
-- ============================================================================
-- Draws a small white circle at every light's world position on the currently
-- selected unit, with the light's name as a tooltip on hover. Clicking a
-- marker selects (loads) that light into the editor.

local LIGHT_MARKER_RADIUS_WORLD = 2    -- circle radius in world elmos (small)
local LIGHT_MARKER_HOVER_PIXELS = 14   -- click/hover hit radius (px)

-- Helper: convert a piece-local (lx,ly,lz) for a given light's pieceIndex on a
-- given unit to world space. Mirrors the math of pieceLocalToWorld but takes
-- pieceIndex directly instead of reading state.pieceName.
local function lightPieceLocalToWorld(unitID, pieceIdx, lx, ly, lz)
	if pieceIdx and pieceIdx > 0 then
		local m = { spGetUnitPieceMatrix(unitID, pieceIdx) }
		if m and m[1] then
			local px = m[1]*lx + m[5]*ly + m[9] *lz + m[13]
			local py = m[2]*lx + m[6]*ly + m[10]*lz + m[14]
			local pz = m[3]*lx + m[7]*ly + m[11]*lz + m[15]
			lx, ly, lz = px, py, pz
		end
	end
	local fx, fy, fz, rx, ry, rz, ux, uy, uz = spGetUnitDirection(unitID)
	local upx, upy, upz = spGetUnitPosition(unitID)
	if not fx or not upx then return nil end
	local wx = -lx * rx + ly * ux + lz * fx + upx
	local wy = -lx * ry + ly * uy + lz * fy + upy
	local wz = -lx * rz + ly * uz + lz * fz + upz
	return wx, wy, wz
end

-- Returns world {x,y,z} of the primary endpoint of a light (lightParamTable[1..3]).
local function getLightWorldPos(unitID, lightTable)
	if not unitID or type(lightTable) ~= "table" or not lightTable.lightParamTable then return nil end
	local p = lightTable.lightParamTable
	return lightPieceLocalToWorld(unitID, lightTable.pieceIndex, p[1] or 0, p[2] or 0, p[3] or 0)
end

-- Returns world {x,y,z} of the SECOND endpoint of a beam light (lightParamTable[5..7]).
-- Returns nil for non-beam lights.
local function getLightSecondWorldPos(unitID, lightTable)
	if not unitID or type(lightTable) ~= "table" or not lightTable.lightParamTable then return nil end
	if lightTable.lightType ~= "beam" then return nil end
	local p = lightTable.lightParamTable
	return lightPieceLocalToWorld(unitID, lightTable.pieceIndex, p[5] or 0, p[6] or 0, p[7] or 0)
end

-- Iterates all lights for the current unit. Yields (lightname, lightTable, wx, wy, wz).
local function forEachLightOnCurrentUnit(fn)
	if not state.currentUnitID or not state.currentUnitDefID then return end
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	if not udLights then return end
	local lights = udLights[state.currentUnitDefID]
	if not lights then return end
	for name, lt in pairs(lights) do
		if type(lt) == "table" then
			local wx, wy, wz = getLightWorldPos(state.currentUnitID, lt)
			if wx then fn(name, lt, wx, wy, wz) end
		end
	end
end

-- Camera-facing line-loop circle. Uses the camera's actual right and up
-- vectors so the circle stays a clean disc no matter the view angle.
local function drawCircleBillboardOutline(x, y, z, radius, segments)
	local cv = Spring.GetCameraVectors()
	if not cv or not cv.right or not cv.up then return end
	local rx, ry, rz = cv.right[1], cv.right[2], cv.right[3]
	local ux, uy, uz = cv.up[1], cv.up[2], cv.up[3]
	gl.BeginEnd(GL.LINE_LOOP, function()
		for i = 0, segments - 1 do
			local a = (i / segments) * math.pi * 2
			local cs = math.cos(a) * radius
			local sn = math.sin(a) * radius
			gl.Vertex(x + rx*cs + ux*sn, y + ry*cs + uy*sn, z + rz*cs + uz*sn)
		end
	end)
end

local function drawLightOverviewMarkers()
	if not state.currentUnitID then return end

	gl.PushAttrib(GL.LINE_BIT)
	gl.DepthTest(false)

	-- Small "billboarded" cross-style markers (cheap, depth-independent).
	-- Hovered light gets a brighter outline + bigger size.
	local hoveredName = state.hoveredLightMarker
	local editingName = state.editingExisting

	forEachLightOnCurrentUnit(function(name, lt, wx, wy, wz)
		local isHovered = (name == hoveredName)
		local isEditing = (name == editingName)
		local isSelected = state.selectedLights[name]
		local r = LIGHT_MARKER_RADIUS_WORLD * (isHovered and 1.7 or ((isEditing or isSelected) and 1.3 or 1.0))

		if isHovered then
			gl.Color(0.6, 1.0, 0.6, 1.0)     -- green on hover
			gl.LineWidth(2)
		elseif isEditing or isSelected then
			gl.Color(1.0, 0.85, 0.25, 1.0)   -- gold = active / selected
			gl.LineWidth(2)
		else
			gl.Color(1, 1, 1, 0.6)            -- neutral white, slightly transparent
			gl.LineWidth(1)
		end
		-- Non-beam: simple billboard circle marker at the light position.
		-- Beam: outline-only capsule (two side rails + half-circle end caps)
		-- so the selector reads as one continuous tube without interior lines.
		if lt.lightType ~= "beam" then
			drawCircleBillboardOutline(wx, wy, wz, r, 32)
		else
			local x2, y2, z2 = getLightSecondWorldPos(state.currentUnitID, lt)
			if x2 then
				local cv = Spring.GetCameraVectors()
				if cv and cv.forward then
					-- Beam axis in world
					local ax, ay, az = x2 - wx, y2 - wy, z2 - wz
					local alen = math.sqrt(ax*ax + ay*ay + az*az)
					if alen > 1e-3 then
						-- Two camera-plane unit vectors:
						--   tdir = beam axis projected onto camera plane (normalized)
						--   pdir = perpendicular to tdir in the camera plane
						local fx, fy, fz = cv.forward[1], cv.forward[2], cv.forward[3]
						-- pdir = forward × axis (camera-plane perpendicular to beam)
						local pdx = fy*az - fz*ay
						local pdy = fz*ax - fx*az
						local pdz = fx*ay - fy*ax
						local plen = math.sqrt(pdx*pdx + pdy*pdy + pdz*pdz)
						if plen > 1e-6 then
							pdx, pdy, pdz = pdx/plen, pdy/plen, pdz/plen
							-- tdir = pdir × forward (back into the camera plane,
							-- now perpendicular to pdir, parallel to projected beam)
							local tdx = pdy*fz - pdz*fy
							local tdy = pdz*fx - pdx*fz
							local tdz = pdx*fy - pdy*fx
							local tlen = math.sqrt(tdx*tdx + tdy*tdy + tdz*tdz)
							if tlen > 1e-6 then
								tdx, tdy, tdz = tdx/tlen, tdy/tlen, tdz/tlen
								-- Now px is perpendicular offset, ox is along-beam outward
								local px, py, pz = pdx*r, pdy*r, pdz*r
								-- Decide which sign of tdir points OUT past the start
								-- (away from the second endpoint). Test by stepping
								-- one unit along tdir from start and seeing if it
								-- moved away from end.
								local probex = wx + tdx
								local probey = wy + tdy
								local probez = wz + tdz
								local d1 = (probex-x2)^2 + (probey-y2)^2 + (probez-z2)^2
								local d2 = (wx-x2)^2 + (wy-y2)^2 + (wz-z2)^2
								local outSign = (d1 > d2) and 1 or -1
								local ox, oy, oz = tdx * r * outSign, tdy * r * outSign, tdz * r * outSign

								-- Side rails
								gl.BeginEnd(GL.LINES, function()
									gl.Vertex(wx + px, wy + py, wz + pz)
									gl.Vertex(x2 + px, y2 + py, z2 + pz)
									gl.Vertex(wx - px, wy - py, wz - pz)
									gl.Vertex(x2 - px, y2 - py, z2 - pz)
								end)
								-- Cap at start: half-circle from +perp through outward to -perp
								local segs = 16
								gl.BeginEnd(GL.LINE_STRIP, function()
									for i = 0, segs do
										local a = (i / segs) * math.pi
										local cs = math.cos(a)
										local sn = math.sin(a)
										gl.Vertex(wx + ox*sn + px*cs,
										         wy + oy*sn + py*cs,
										         wz + oz*sn + pz*cs)
									end
								end)
								-- Cap at end: outward is the OTHER direction
								gl.BeginEnd(GL.LINE_STRIP, function()
									for i = 0, segs do
										local a = (i / segs) * math.pi
										local cs = math.cos(a)
										local sn = math.sin(a)
										gl.Vertex(x2 - ox*sn + px*cs,
										         y2 - oy*sn + py*cs,
										         z2 - oz*sn + pz*cs)
									end
								end)
							end
						end
					end
				end
			end
		end
	end)

	gl.DepthTest(true)
	gl.Color(1, 1, 1, 1)
	gl.PopAttrib()
end

local function drawLightOverviewLabel()
	-- Draw the hovered light's name as a screen-space label near the cursor.
	if not state.hoveredLightMarker then return end
	local mx, my = Spring.GetMouseState()
	gl.PushMatrix()
	gl.Color(0, 0, 0, 0.7)
	-- Text background: approximate width by char count
	local label = state.hoveredLightMarker
	local fontSize = 14
	local approxW = #label * fontSize * 0.55
	gl.Rect(mx + 12, my - 4, mx + 12 + approxW + 8, my + fontSize + 4)
	gl.Color(1, 0.9, 0.4, 1)
	gl.Text(label, mx + 16, my + 2, fontSize, "o")
	gl.PopMatrix()
end

local function hitTestLightMarker(mx, my)
	if not state.currentUnitID then return nil end
	local best, bestDist = nil, LIGHT_MARKER_HOVER_PIXELS * LIGHT_MARKER_HOVER_PIXELS
	forEachLightOnCurrentUnit(function(name, lt, wx, wy, wz)
		local sx, sy = spWorldToScreenCoords(wx, wy, wz)
		if not sx then return end

		if lt.lightType == "beam" then
			-- Beam: hit-test against the entire screen-space line segment so
			-- clicking anywhere along the beam selects it.
			local x2, y2, z2 = getLightSecondWorldPos(state.currentUnitID, lt)
			if x2 then
				local sx2, sy2 = spWorldToScreenCoords(x2, y2, z2)
				if sx2 then
					local d = distSqPointToSegment(mx, my, sx, sy, sx2, sy2)
					if d < bestDist then
						best = name
						bestDist = d
					end
					return
				end
			end
		end

		-- Non-beam (or beam with off-screen second endpoint): point distance.
		local dx, dy = sx - mx, sy - my
		local d = dx*dx + dy*dy
		if d < bestDist then
			best = name
			bestDist = d
		end
	end)
	return best
end

-- Computes world centroid of all selected lights. Returns x,y,z or nil if
-- nothing selected or selection lights have different pieces (we require a
-- shared piece for multi-move so the gizmo is meaningful).
function getMultiCentroid()
	local sharedPiece = getSelectedSharedPiece()
	if not sharedPiece then return nil end
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	if not udLights or not state.currentUnitDefID then return nil end
	local unitLights = udLights[state.currentUnitDefID]
	if not unitLights then return nil end
	local sx, sy, sz, n = 0, 0, 0, 0
	for name, _ in pairs(state.selectedLights) do
		local lt = unitLights[name]
		if type(lt) == "table" then
			local wx, wy, wz = getLightWorldPos(state.currentUnitID, lt)
			if wx then
				sx, sy, sz = sx + wx, sy + wy, sz + wz
				n = n + 1
			end
		end
	end
	if n == 0 then return nil end
	return sx / n, sy / n, sz / n
end

function widget:DrawWorld()
	if not state.currentUnitID then return end
	if state.pieceName and state.pieceName ~= "" then
		drawPieceMarker(state.currentUnitID, state.pieceName, 1.0, 0.85, 0.2)
	end
	if state.highlightPiece and state.highlightPiece ~= state.pieceName then
		drawPieceMarker(state.currentUnitID, state.highlightPiece, 0.3, 1.0, 0.4)
	end
	-- Light overview markers (small white circles on every light of this unit)
	drawLightOverviewMarkers()
	-- Multi-select gizmo (centroid of all selected lights, only if shared piece)
	if isMultiMode() then
		local cx, cy, cz = getMultiCentroid()
		if cx then
			gl.PushAttrib(GL.LINE_BIT)
			gl.DepthTest(false)
			drawGizmoAt(cx, cy, cz, "multi")
			gl.DepthTest(true)
			gl.PopAttrib()
		end
	end
	-- Always draw light marker when a unit is selected, regardless of panel state
	drawLightMarker()
end

