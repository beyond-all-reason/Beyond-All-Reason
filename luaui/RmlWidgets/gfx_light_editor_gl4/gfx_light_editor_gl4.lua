local function loadExistingLight(name)
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	if not udLights or not state.currentUnitDefID then return false end
	local lights = udLights[state.currentUnitDefID]
	if not lights or type(lights[name]) ~= "table" then return false end
	local lt = lights[name]

	state.lightName = name
	state.pieceName = lt.pieceName or ""

	for k, v in pairs(defaultLight) do
		state.values[k] = v
	end
	if lt.lightConfig then
		for k, _ in pairs(defaultLight) do
			if lt.lightConfig[k] ~= nil then state.values[k] = lt.lightConfig[k] end
		end
		-- For point lights, dir/theta in lightConfig actually mean
		-- world-animation params (mapped to pdir/ptheta in our editor).
		if (lt.lightType or "point") == "point" then
			if lt.lightConfig.dirx ~= nil then state.values.pdirx = lt.lightConfig.dirx end
			if lt.lightConfig.diry ~= nil then state.values.pdiry = lt.lightConfig.diry end
			if lt.lightConfig.dirz ~= nil then state.values.pdirz = lt.lightConfig.dirz end
			if lt.lightConfig.theta ~= nil then state.values.ptheta = lt.lightConfig.theta end
		end
	elseif lt.lightParamTable then
		local p = lt.lightParamTable
		state.values.posx = p[1]; state.values.posy = p[2]; state.values.posz = p[3]; state.values.radius = p[4]
		state.values.r = p[9]; state.values.g = p[10]; state.values.b = p[11]; state.values.a = p[12]
		state.values.modelfactor = p[13]; state.values.specular = p[14]; state.values.scattering = p[15]; state.values.lensflare = p[16]
		state.values.lifetime = p[18]; state.values.sustain = p[19]; state.values.selfshadowing = p[20]
		state.values.color2r = p[21]; state.values.color2g = p[22]; state.values.color2b = p[23]; state.values.colortime = p[24]
		if (lt.lightType or "point") == "cone" then
			state.values.dirx = p[5]; state.values.diry = p[6]; state.values.dirz = p[7]; state.values.theta = p[8]
		elseif lt.lightType == "beam" then
			state.values.pos2x = p[5]; state.values.pos2y = p[6]; state.values.pos2z = p[7]
		else
			-- Point light: slots 5-8 hold world-animation params.
			state.values.pdirx = p[5]; state.values.pdiry = p[6]; state.values.pdirz = p[7]; state.values.ptheta = p[8]
		end
	end

	-- Set state BEFORE updating UI: applyAllValuesToUI() triggers onSlider
	-- which calls applyPreview(), and that needs the correct lightType + instance ID
	-- to replace the existing light in-place rather than duplicate it.
	state.editingExisting = name
	state.lightType = lt.lightType or "point"
	applyAllValuesToUI()
	if state.document then
		local pieceDisp = state.document:GetElementById("le-piece-display")
		if pieceDisp then pieceDisp.inner_rml = (state.pieceName ~= "" and state.pieceName) or "(world)" end
		local existDisp = state.document:GetElementById("le-existing-display")
		if existDisp then existDisp.inner_rml = name end
	end
	setLightType(state.lightType)
	setStatus("Loaded: "..name)
	return true
end

local spGetUnitPieceInfo = Spring.GetUnitPieceInfo

local function pieceRadius(unitID, pieceIdx)
	local info = spGetUnitPieceInfo and spGetUnitPieceInfo(unitID, pieceIdx) or nil
	if not info or not info.min or not info.max then return 4 end
	local dx = (info.max[1] or 0) - (info.min[1] or 0)
	local dy = (info.max[2] or 0) - (info.min[2] or 0)
	local dz = (info.max[3] or 0) - (info.min[3] or 0)
	local r = math.sqrt(dx*dx + dy*dy + dz*dz) * 0.5
	if r < 1 then r = 1 end
	return r
end

function escapeKey(s)
	if string.match(s, "^[%a_][%w_]*$") then return s end
	return "['"..string.gsub(s, "'", "\\'").."']"
end

function fmt(n)
	if type(n) ~= "number" then return tostring(n) end
	if n == math.floor(n) and math.abs(n) < 1e9 then
		return tostring(math.floor(n))
	end
	return string.format("%.4g", n)
end

local function generateSnippet()
	local v = state.values
	local lines = {}
	local indent = "\t\t"
	local cfgIndent = "\t\t\t\t\t\t"

	lines[#lines+1] = string.format("%s%s = {", indent, escapeKey(state.lightName))
	lines[#lines+1] = string.format("%s\tlightType = '%s',", indent, state.lightType)
	if state.pieceName ~= "" then
		lines[#lines+1] = string.format("%s\tpieceName = '%s',", indent, state.pieceName)
	end
	lines[#lines+1] = string.format("%s\tlightConfig = { posx = %s, posy = %s, posz = %s, radius = %s,",
		indent, fmt(v.posx), fmt(v.posy), fmt(v.posz), fmt(v.radius))

	if state.lightType == "cone" then
		lines[#lines+1] = string.format("%sdirx = %s, diry = %s, dirz = %s, theta = %s,",
			cfgIndent, fmt(v.dirx), fmt(v.diry), fmt(v.dirz), fmt(v.theta))
	elseif state.lightType == "beam" then
		lines[#lines+1] = string.format("%spos2x = %s, pos2y = %s, pos2z = %s,",
			cfgIndent, fmt(v.pos2x), fmt(v.pos2y), fmt(v.pos2z))
	else
		-- Optional point world-animation; only emit when any param is non-zero.
		if (v.pdirx or 0) ~= 0 or (v.pdiry or 0) ~= 0 or (v.pdirz or 0) ~= 0 or (v.ptheta or 0) ~= 0 then
			lines[#lines+1] = string.format("%sdirx = %s, diry = %s, dirz = %s, theta = %s,",
				cfgIndent, fmt(v.pdirx or 0), fmt(v.pdiry or 0), fmt(v.pdirz or 0), fmt(v.ptheta or 0))
		end
	end
	-- color2 / colortime applies to all light types (point/cone/beam).
	lines[#lines+1] = string.format("%scolor2r = %s, color2g = %s, color2b = %s, colortime = %s,",
		cfgIndent, fmt(v.color2r), fmt(v.color2g), fmt(v.color2b), fmt(v.colortime))

	lines[#lines+1] = string.format("%sr = %s, g = %s, b = %s, a = %s,",
		cfgIndent, fmt(v.r), fmt(v.g), fmt(v.b), fmt(v.a))
	lines[#lines+1] = string.format("%smodelfactor = %s, specular = %s, scattering = %s, lensflare = %s,",
		cfgIndent, fmt(v.modelfactor), fmt(v.specular), fmt(v.scattering), fmt(v.lensflare))
	lines[#lines+1] = string.format("%slifetime = %s, sustain = %s, selfshadowing = %s},",
		cfgIndent, fmt(math.floor(v.lifetime or 0)), fmt(v.sustain), fmt(v.selfshadowing))
	lines[#lines+1] = string.format("%s},", indent)

	return table.concat(lines, "\n")
end

