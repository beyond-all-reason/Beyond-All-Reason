local function drawPieceMarker(unitID, pieceName, r, g, b)
	if not unitID or not pieceName or not state.pieceMap then return end
	local idx = state.pieceMap[pieceName]
	if not idx then return end
	local px, py, pz = spGetUnitPiecePosition(unitID, idx)
	if not px then return end
	local rad = pieceRadius(unitID, idx)

	gl.PushAttrib(GL.LINE_BIT)
	gl.LineWidth(2)
	gl.Color(r, g, b, 1)
	gl.DepthTest(false)

	gl.BeginEnd(GL.LINES, function()
		gl.Vertex(px - rad, py, pz); gl.Vertex(px + rad, py, pz)
		gl.Vertex(px, py - rad, pz); gl.Vertex(px, py + rad, pz)
		gl.Vertex(px, py, pz - rad); gl.Vertex(px, py, pz + rad)
	end)

	gl.BeginEnd(GL.LINE_LOOP, function()
		gl.Vertex(px + rad, py, pz)
		gl.Vertex(px, py + rad, pz)
		gl.Vertex(px - rad, py, pz)
		gl.Vertex(px, py - rad, pz)
	end)
	gl.BeginEnd(GL.LINE_LOOP, function()
		gl.Vertex(px + rad, py, pz)
		gl.Vertex(px, py, pz + rad)
		gl.Vertex(px - rad, py, pz)
		gl.Vertex(px, py, pz - rad)
	end)
	gl.BeginEnd(GL.LINE_LOOP, function()
		gl.Vertex(px, py + rad, pz)
		gl.Vertex(px, py, pz + rad)
		gl.Vertex(px, py - rad, pz)
		gl.Vertex(px, py, pz - rad)
	end)

	gl.DepthTest(true)
	gl.Color(1, 1, 1, 1)
	gl.PopAttrib()
end

-- Transforms a point given in piece-local coords to world coords using the
-- piece's full 4x4 transformation matrix (the same one the deferred rendering
-- shader applies to attached lights). Returns world x, y, z.
--
-- When no piece is selected, falls back to the unit's own world transform via
-- the root piece (index 1 in BAR's 1-based pieceIndex API).
-- The piece-matrix from Spring is local to the unit (piece offset + skeletal
-- animation), NOT a full world matrix. The deferred-rendering shader prepends
-- the unit's world transform: world = unitWorldMatrix * pieceMatrix * lightPos.
-- We replicate that here: first apply piece-local matrix, then rotate+translate
-- by the unit's world transform.
local function pieceLocalToWorld(lx, ly, lz)
	if not state.currentUnitID then return nil end

	local pieceIdx
	if state.pieceMap and state.pieceName ~= "" and state.pieceMap[state.pieceName] then
		pieceIdx = state.pieceMap[state.pieceName]
	end

	-- Step 1: piece-local transform (turret rotation, attachment offset, etc).
	local m = pieceIdx and { spGetUnitPieceMatrix(state.currentUnitID, pieceIdx) } or nil
	local px, py, pz
	if m and m[1] then
		-- Column-major: m[1..4]=col0, m[5..8]=col1, m[9..12]=col2, m[13..16]=col3 (translation)
		px = m[1]*lx + m[5]*ly + m[9] *lz + m[13]
		py = m[2]*lx + m[6]*ly + m[10]*lz + m[14]
		pz = m[3]*lx + m[7]*ly + m[11]*lz + m[15]
	else
		-- No piece selected: light is in unit-local space directly.
		px, py, pz = lx, ly, lz
	end

	-- Step 2: unit world transform. GetUnitDirection gives front/right/up basis.
	local fx, fy, fz, rx, ry, rz, ux, uy, uz = spGetUnitDirection(state.currentUnitID)
	local upx, upy, upz = spGetUnitPosition(state.currentUnitID)
	if not fx or not upx then return nil end

	-- Spring/BAR piece-local convention: model +X is the unit's *left* side
	-- (mirrored from intuitive). We negate the X contribution to compensate.
	local wx = -px * rx + py * ux + pz * fx + upx
	local wy = -px * ry + py * uy + pz * fy + upy
	local wz = -px * rz + py * uz + pz * fz + upz
	return wx, wy, wz
end

-- Same as pieceLocalToWorld but for a direction vector (no translation).
local function pieceLocalDirToWorld(lx, ly, lz)
	if not state.currentUnitID then return lx, ly, lz end

	local pieceIdx
	if state.pieceMap and state.pieceName ~= "" and state.pieceMap[state.pieceName] then
		pieceIdx = state.pieceMap[state.pieceName]
	end

	local m = pieceIdx and { spGetUnitPieceMatrix(state.currentUnitID, pieceIdx) } or nil
	local px, py, pz
	if m and m[1] then
		px = m[1]*lx + m[5]*ly + m[9] *lz
		py = m[2]*lx + m[6]*ly + m[10]*lz
		pz = m[3]*lx + m[7]*ly + m[11]*lz
	else
		px, py, pz = lx, ly, lz
	end

	local fx, fy, fz, rx, ry, rz, ux, uy, uz = spGetUnitDirection(state.currentUnitID)
	if not fx then return px, py, pz end
	-- Mirror X to match BAR's piece-local convention.
	local wx = -px * rx + py * ux + pz * fx
	local wy = -px * ry + py * uy + pz * fy
	local wz = -px * rz + py * uz + pz * fz
	return wx, wy, wz
end

local function getLightPosWorld()
	local v = state.values
	return pieceLocalToWorld(v.posx or 0, v.posy or 0, v.posz or 0)
end

