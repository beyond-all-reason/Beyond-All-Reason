local function clearChildren(element)
	if not element then return end
	-- Try inner_rml first (PR #7219 pattern); fall back to RemoveChild if needed.
	pcall(function() element.inner_rml = "" end)
	-- Some RmlUi bindings keep <option> children even after inner_rml clear; force-remove.
	local guard = 0
	while element.first_child and guard < 200 do
		pcall(function() element:RemoveChild(element.first_child) end)
		guard = guard + 1
	end
end

-- Escape arbitrary string to safe RML attribute value (just enough for option
-- values: quotes and angle brackets). Light/piece names in BAR are simple
-- identifiers, but be defensive.
local function rmlEscape(s)
	s = tostring(s)
	s = s:gsub("&", "&amp;"):gsub("<", "&lt;"):gsub(">", "&gt;"):gsub('"', "&quot;")
	return s
end

-- Build all <option> children as a single RML string and assign via inner_rml.
-- This is the only reliable way to fully replace a <select>'s options in
-- BAR's RmlUi binding -- per-element RemoveChild loops have proven unreliable.
-- Populate a <div> dropdown list with clickable items. Each option becomes
-- a <div class="le-dropdown-item" data-event-click="onAction('action', 'value')">label</div>.
-- We use plain <div>s instead of <select>/<option> because BAR's RmlUi binding
-- does not reliably clear <option> children, leading to runaway dropdowns.
local function setListOptions(listEl, options, actionName)
	if not listEl then return end
	local parts = {}
	local itemStyle = "display: block; height: 22dp; line-height: 22dp; padding: 0dp 10dp; margin-bottom: 2dp; color: #ddd; font-size: 12dp; cursor: pointer; border: 1dp #444; background-color: #2a2a2a;"
	for _, opt in ipairs(options) do
		parts[#parts + 1] = string.format(
			'<div class="le-dropdown-item" style="%s" data-event-click="onAction(\'%s\', \'%s\')">%s</div>',
			itemStyle, rmlEscape(actionName), rmlEscape(opt.value), rmlEscape(opt.label))
	end
	-- inner_rml on <div> is reliable in BAR's RmlUi (verified in PR #7219).
	listEl.inner_rml = table.concat(parts)
end

