-- Returns the shared pieceName if all selected lights share one piece,
-- else nil. Lights with no pieceName share the special "" key (= world-attached).
local function getSelectedSharedPiece()
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	if not udLights or not state.currentUnitDefID then return nil end
	local unitLights = udLights[state.currentUnitDefID]
	if not unitLights then return nil end
	local sharedPiece, hasAny
	for name, _ in pairs(state.selectedLights) do
		local lt = unitLights[name]
		if type(lt) == "table" then
			local p = lt.pieceName or ""
			if not hasAny then
				sharedPiece = p
				hasAny = true
			elseif p ~= sharedPiece then
				return nil
			end
		end
	end
	return hasAny and sharedPiece or nil
end

-- When editing an existing light, mirror the deferred-rendering widget's
-- instance ID (`tostring(unitID) .. lightname`) so we replace it in-place
-- rather than create a duplicate.
function getPreviewInstanceID()
	if state.editingExisting and state.currentUnitID then
		return tostring(state.currentUnitID) .. state.editingExisting
	end
	return PREVIEW_INSTANCE_ID_NEW
end

local function buildLightParamTable()
	local v = state.values
	local t = {}
	for i = 1, lightParamSize do t[i] = 0 end
	t[1]  = v.posx or 0
	t[2]  = v.posy or 0
	t[3]  = v.posz or 0
	t[4]  = v.radius or 0
	t[9]  = v.r or 0
	t[10] = v.g or 0
	t[11] = v.b or 0
	t[12] = v.a or 0
	t[13] = v.modelfactor or 0
	t[14] = v.specular or 0
	t[15] = v.scattering or 0
	t[16] = v.lensflare or 0
	t[18] = math.floor(v.lifetime or 0)
	t[19] = v.sustain or 0
	t[20] = v.selfshadowing or 0
	t[21] = v.color2r or 0
	t[22] = v.color2g or 0
	t[23] = v.color2b or 0
	t[24] = v.colortime or 0
	if state.lightType == "cone" then
		t[5] = v.dirx or 0
		t[6] = v.diry or 0
		t[7] = v.dirz or 0
		t[8] = v.theta or 0
	elseif state.lightType == "beam" then
		t[5] = v.pos2x or 0
		t[6] = v.pos2y or 0
		t[7] = v.pos2z or 0
	else
		-- Point light: optional world-animation params live in slots 5-8.
		-- (color2r/g/b/colortime live in slots 21-24, written above.)
		t[5] = v.pdirx or 0
		t[6] = v.pdiry or 0
		t[7] = v.pdirz or 0
		t[8] = v.ptheta or 0
	end
	return t
end

function getVBOForType(lightType)
	local api = WG['lightsgl4']
	if not api or not api.GetLightVBOMap then return nil end
	local map = api.GetLightVBOMap()
	return map and map[lightType] or nil
end

local function clearPreview()
	local api = WG['lightsgl4']
	if not api or not api.RemoveLight then return end
	-- Always try to remove BOTH the "new light" preview ID and the
	-- existing-light's natural ID. This catches the case where the user
	-- switches from a "new light" preview to editing an existing one
	-- (or vice versa) and the previously-spawned preview should die.
	local idsToTry = { PREVIEW_INSTANCE_ID_NEW }
	local currentID = getPreviewInstanceID()
	if currentID ~= PREVIEW_INSTANCE_ID_NEW then
		idsToTry[#idsToTry + 1] = currentID
	end
	for _, t in ipairs({"point","cone","beam"}) do
		local vbo = getVBOForType(t)
		if vbo and vbo.instanceIDtoIndex then
			for _, id in ipairs(idsToTry) do
				if vbo.instanceIDtoIndex[id] then
					api.RemoveLight(t, id, state.currentUnitID)
				end
			end
		end
	end
end

local function updateSwatch()
	if not state.document then return end
	local sw = state.document:GetElementById("le-color-swatch")
	if not sw then return end
	local r = math.max(0, math.min(255, math.floor((state.values.r or 0) * 255)))
	local g = math.max(0, math.min(255, math.floor((state.values.g or 0) * 255)))
	local b = math.max(0, math.min(255, math.floor((state.values.b or 0) * 255)))
	sw:SetAttribute("style", string.format("background-color: #%02X%02X%02X;", r, g, b))
end

local function applyPreview()
	updateSwatch()
	local api = WG['lightsgl4']
	if not api or not api.AddLight then return end
	local vbo = getVBOForType(state.lightType)
	if not vbo then return end
	clearPreview()

	local pieceIndex = 0
	if state.currentUnitID and state.pieceMap and state.pieceName ~= "" then
		pieceIndex = state.pieceMap[state.pieceName] or 0
	end
	local params = buildLightParamTable()
	if state.currentUnitID then
		api.AddLight(getPreviewInstanceID(), state.currentUnitID, pieceIndex, vbo, params)

		-- Keep the runtime lightTable in sync so the overview-marker (which
		-- reads from lightParamTable) tracks the live edit. Without this the
		-- white circle would stay at the original config position while the
		-- actual rendered light moved with the editor's preview.
		if state.editingExisting and state.currentUnitDefID and api.GetUnitDefLights then
			local udLights = api.GetUnitDefLights()
			local lt = udLights and udLights[state.currentUnitDefID] and udLights[state.currentUnitDefID][state.editingExisting]
			if type(lt) == "table" and lt.lightParamTable then
				for i = 1, #params do lt.lightParamTable[i] = params[i] end
				lt.pieceIndex = pieceIndex
				if state.pieceName ~= "" then lt.pieceName = state.pieceName end
				lt.lightType = state.lightType
			end
		end
		-- Track this edit in the overrides table so a Save persists it.
		if state.currentUnitName and state.lightName and state.lightName ~= "" then
			recordCurrentAsOverride()
		end
	end
end

