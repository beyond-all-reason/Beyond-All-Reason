-- Format a number for the config file: integers without trailing .0,
-- floats with trimmed precision.
local function fmtConfigNum(n)
	if type(n) ~= "number" then return tostring(n) end
	if n == math.floor(n) and math.abs(n) < 1e9 then
		return tostring(math.floor(n))
	end
	return string.format("%.4g", n)
end

-- Lua identifier-safe key (else quote it).
local function luaKey(s)
	if type(s) == "string" and string.match(s, "^[%a_][%w_]*$") then return s end
	return "['"..tostring(s):gsub("'", "\\'").."']"
end

-- Serialize a single light table (the value the user pastes today).
-- Format matches the existing config style.
local function serializeLight(name, lt)
	local lc = lt.lightConfig or {}
	local lines = {}
	lines[#lines+1] = string.format("\t\t\t%s = {", luaKey(name))
	lines[#lines+1] = string.format("\t\t\t\tlightType = '%s',", lt.lightType or "point")
	if lt.pieceName and lt.pieceName ~= "" then
		lines[#lines+1] = string.format("\t\t\t\tpieceName = '%s',", lt.pieceName)
	end
	-- lightConfig: pos/radius first, then type-specific, then color, then misc
	local function f(k) return fmtConfigNum(lc[k] or 0) end
	lines[#lines+1] = string.format("\t\t\t\tlightConfig = { posx = %s, posy = %s, posz = %s, radius = %s,",
		f("posx"), f("posy"), f("posz"), f("radius"))
	if (lt.lightType or "point") == "cone" then
		lines[#lines+1] = string.format("\t\t\t\t\t\tdirx = %s, diry = %s, dirz = %s, theta = %s,",
			f("dirx"), f("diry"), f("dirz"), f("theta"))
	elseif lt.lightType == "beam" then
		lines[#lines+1] = string.format("\t\t\t\t\t\tpos2x = %s, pos2y = %s, pos2z = %s,",
			f("pos2x"), f("pos2y"), f("pos2z"))
	else
		lines[#lines+1] = string.format("\t\t\t\t\t\tcolor2r = %s, color2g = %s, color2b = %s, colortime = %s,",
			f("color2r"), f("color2g"), f("color2b"), f("colortime"))
	end
	lines[#lines+1] = string.format("\t\t\t\t\t\tr = %s, g = %s, b = %s, a = %s,",
		f("r"), f("g"), f("b"), f("a"))
	lines[#lines+1] = string.format("\t\t\t\t\t\tmodelfactor = %s, specular = %s, scattering = %s, lensflare = %s,",
		f("modelfactor"), f("specular"), f("scattering"), f("lensflare"))
	lines[#lines+1] = string.format("\t\t\t\t\t\tlifetime = %s, sustain = %s, selfshadowing = %s},",
		fmtConfigNum(math.floor(lc.lifetime or 0)), f("sustain"), f("selfshadowing"))
	lines[#lines+1] = "\t\t\t},"
	return table.concat(lines, "\n")
end

-- Build the full override-block content as a string. It contains a Lua table
-- definition + apply-loop, so that when DeferredLightsGL4config.lua loads,
-- the engine merges these overrides into unitDefLights (and removes deletions).
local function buildOverrideBlock(overrides)
	local body = {}
	body[#body+1] = OVERRIDE_BEGIN
	body[#body+1] = "local _editorOverrides = {"
	-- Sort unitnames for stable diffs
	local unitNames = {}
	for u, _ in pairs(overrides) do unitNames[#unitNames+1] = u end
	table.sort(unitNames)
	for _, unitname in ipairs(unitNames) do
		local lights = overrides[unitname]
		body[#body+1] = string.format("\t%s = {", luaKey(unitname))
		local lightNames = {}
		for n, _ in pairs(lights) do lightNames[#lightNames+1] = n end
		table.sort(lightNames)
		for _, lightname in ipairs(lightNames) do
			local entry = lights[lightname]
			if entry == "__deleted__" then
				body[#body+1] = string.format("\t\t%s = '__deleted__',", luaKey(lightname))
			else
				body[#body+1] = serializeLight(lightname, entry)
			end
		end
		body[#body+1] = "\t},"
	end
	body[#body+1] = "}"
	body[#body+1] = ""
	body[#body+1] = "-- Apply overrides to the live unitDefLights table."
	body[#body+1] = "for unitname, lights in pairs(_editorOverrides) do"
	body[#body+1] = "\tif UnitDefNames[unitname] then"
	body[#body+1] = "\t\tlocal udID = UnitDefNames[unitname].id"
	body[#body+1] = "\t\tunitDefLights[udID] = unitDefLights[udID] or {}"
	body[#body+1] = "\t\tfor lightname, ldef in pairs(lights) do"
	body[#body+1] = "\t\t\tif ldef == '__deleted__' then"
	body[#body+1] = "\t\t\t\tunitDefLights[udID][lightname] = nil"
	body[#body+1] = "\t\t\telse"
	body[#body+1] = "\t\t\t\tunitDefLights[udID][lightname] = ldef"
	body[#body+1] = "\t\t\tend"
	body[#body+1] = "\t\tend"
	body[#body+1] = "\tend"
	body[#body+1] = "end"
	body[#body+1] = OVERRIDE_END
	return table.concat(body, "\n")
end

-- Resolve the on-disk absolute path of the config file. Lua's io.open uses
-- OS file paths, not Spring VFS paths -- VFS.GetFileAbsolutePath does the
-- translation. Cached after the first lookup.
local function getConfigDiskPath()
	if state.configDiskPath then return state.configDiskPath end
	if VFS and VFS.GetFileAbsolutePath then
		local abs = VFS.GetFileAbsolutePath(CONFIG_PATH)
		if abs then
			-- Normalize backslashes to forward slashes; both work on Windows
			-- but mixed separators sometimes confuse Lua's io.open.
			abs = abs:gsub("\\", "/")
			state.configDiskPath = abs
			return abs
		end
	end
	-- Fallback: try the relative path as-is. May land in Spring's CWD.
	state.configDiskPath = CONFIG_PATH
	return CONFIG_PATH
end

-- Read the entire config file. Returns content string or nil + error message.
local function readConfigFile()
	local path = getConfigDiskPath()
	-- Prefer reading via VFS (works across .sdd/.sdz and respects mode chain),
	-- since the file is under a game archive on disk.
	if VFS and VFS.LoadFile then
		local content = VFS.LoadFile(CONFIG_PATH, VFS.RAW)
		if content then return content end
	end
	local f, err = io.open(path, "rb")
	if not f then return nil, err end
	local content = f:read("*a")
	f:close()
	return content
end

-- Write the config file with a .bak backup of the previous content.
-- Uses the absolute disk path; io.open with relative paths writes to Spring's
-- CWD (the engine root), which is not what we want.
local function writeConfigFile(content)
	local path = getConfigDiskPath()
	if not path or path == CONFIG_PATH then
		return false, "Could not resolve absolute path for "..CONFIG_PATH
	end
	-- Backup first
	local existing = readConfigFile()
	if existing then
		local bak = io.open(path..".bak", "wb")
		if bak then bak:write(existing); bak:close() end
	end
	local f, err = io.open(path, "wb")
	if not f then return false, err end
	f:write(content)
	f:close()
	return true
end

-- Replaces (or inserts) the override block in the file's content. The block
-- always lives directly above `return allLights`. Returns the new content.
local function replaceOverrideBlock(content, newBlock)
	-- If a block already exists, replace it.
	local beginPos = content:find(OVERRIDE_BEGIN, 1, true)
	local endPos   = content:find(OVERRIDE_END, beginPos or 1, true)
	if beginPos and endPos then
		local endLineEnd = content:find("\n", endPos, true) or (endPos + #OVERRIDE_END)
		return content:sub(1, beginPos - 1) .. newBlock .. content:sub(endLineEnd)
	end
	-- Otherwise insert before `return allLights`.
	-- Match either standalone or with leading whitespace.
	local insertAt = content:find("\nreturn allLights", 1, true)
	if not insertAt then
		-- fallback: append at end
		return content .. "\n\n" .. newBlock .. "\n"
	end
	return content:sub(1, insertAt) .. "\n" .. newBlock .. "\n" .. content:sub(insertAt + 1)
end

-- Build a fresh lightConfig table from the current state.values. Used when
-- recording overrides so the saved file has clean human-readable values
-- instead of the engine's flat lightParamTable form.
local function makeLightConfigFromState()
	local v = state.values
	local lc = {
		posx = v.posx or 0, posy = v.posy or 0, posz = v.posz or 0, radius = v.radius or 0,
		r = v.r or 0, g = v.g or 0, b = v.b or 0, a = v.a or 0,
		modelfactor = v.modelfactor or 0, specular = v.specular or 0,
		scattering = v.scattering or 0, lensflare = v.lensflare or 0,
		lifetime = v.lifetime or 0, sustain = v.sustain or 0, selfshadowing = v.selfshadowing or 0,
	}
	if state.lightType == "cone" then
		lc.dirx = v.dirx or 0; lc.diry = v.diry or 0; lc.dirz = v.dirz or 1; lc.theta = v.theta or 0.5
	elseif state.lightType == "beam" then
		lc.pos2x = v.pos2x or 0; lc.pos2y = v.pos2y or 0; lc.pos2z = v.pos2z or 0
	else
		lc.color2r = v.color2r or 0; lc.color2g = v.color2g or 0
		lc.color2b = v.color2b or 0; lc.colortime = v.colortime or 0
	end
	return lc
end

-- Records the current editor state as an override under the active unit's
-- name + the active light name (existing or new). No-op if no unit selected.
local function recordCurrentAsOverride()
	if not state.currentUnitName or not state.lightName or state.lightName == "" then return end
	state.overrides[state.currentUnitName] = state.overrides[state.currentUnitName] or {}
	state.overrides[state.currentUnitName][state.lightName] = {
		lightType = state.lightType,
		pieceName = (state.pieceName ~= "" and state.pieceName) or nil,
		lightConfig = makeLightConfigFromState(),
	}
end

-- Counts total lights+deletions across all units in the overrides table.
local function countOverrideEntries(overrides)
	local n = 0
	for _, lights in pairs(overrides) do
		for _ in pairs(lights) do n = n + 1 end
	end
	return n
end

-- Convert a runtime lightTable's flat lightParamTable back to a human-readable
-- lightConfig (since the engine nils out lightConfig after init).
local function lightTableToConfig(lt)
	if lt.lightConfig then return lt.lightConfig end
	local p = lt.lightParamTable
	if not p then return {} end
	local lc = {
		posx = p[1] or 0, posy = p[2] or 0, posz = p[3] or 0, radius = p[4] or 0,
		r = p[9] or 0, g = p[10] or 0, b = p[11] or 0, a = p[12] or 0,
		modelfactor = p[13] or 0, specular = p[14] or 0,
		scattering = p[15] or 0, lensflare = p[16] or 0,
		lifetime = p[18] or 0, sustain = p[19] or 0, selfshadowing = p[20] or 0,
		color2r = p[21] or 0, color2g = p[22] or 0,
		color2b = p[23] or 0, colortime = p[24] or 0,
	}
	if lt.lightType == "cone" then
		lc.dirx = p[5] or 0; lc.diry = p[6] or 0; lc.dirz = p[7] or 0; lc.theta = p[8] or 0.5
	elseif lt.lightType == "beam" then
		lc.pos2x = p[5] or 0; lc.pos2y = p[6] or 0; lc.pos2z = p[7] or 0
	else
		-- Point: slots 5-8 are world-animation params (dir/theta).
		lc.dirx = p[5] or 0; lc.diry = p[6] or 0; lc.dirz = p[7] or 0; lc.theta = p[8] or 0
	end
	return lc
end

-- Serialize a single light using the canonical config indentation
-- (2 leading tabs + 6 inner tabs, matching DeferredLightsGL4config.lua style).
local function snippetForLight(name, lt)
	local lc = lightTableToConfig(lt)
	local indent = "\t\t"
	local cfgIndent = "\t\t\t\t\t\t"
	local lines = {}
	lines[#lines+1] = string.format("%s%s = {", indent, escapeKey(name))
	lines[#lines+1] = string.format("%s\tlightType = '%s',", indent, lt.lightType or "point")
	if lt.pieceName and lt.pieceName ~= "" then
		lines[#lines+1] = string.format("%s\tpieceName = '%s',", indent, lt.pieceName)
	end
	local function f(k) return fmt(lc[k] or 0) end
	lines[#lines+1] = string.format("%s\tlightConfig = { posx = %s, posy = %s, posz = %s, radius = %s,",
		indent, f("posx"), f("posy"), f("posz"), f("radius"))
	if (lt.lightType or "point") == "cone" then
		lines[#lines+1] = string.format("%sdirx = %s, diry = %s, dirz = %s, theta = %s,",
			cfgIndent, f("dirx"), f("diry"), f("dirz"), f("theta"))
	elseif lt.lightType == "beam" then
		lines[#lines+1] = string.format("%spos2x = %s, pos2y = %s, pos2z = %s,",
			cfgIndent, f("pos2x"), f("pos2y"), f("pos2z"))
	else
		-- Point-light world-animation params (slots 5-8 under dirx/diry/dirz/theta).
		local hasAnim = (lc.dirx or 0) ~= 0 or (lc.diry or 0) ~= 0 or
		                (lc.dirz or 0) ~= 0 or (lc.theta or 0) ~= 0
		if hasAnim then
			lines[#lines+1] = string.format("%sdirx = %s, diry = %s, dirz = %s, theta = %s,",
				cfgIndent, f("dirx"), f("diry"), f("dirz"), f("theta"))
		end
	end
	-- color2 / colortime applies to all light types.
	lines[#lines+1] = string.format("%scolor2r = %s, color2g = %s, color2b = %s, colortime = %s,",
		cfgIndent, f("color2r"), f("color2g"), f("color2b"), f("colortime"))
	lines[#lines+1] = string.format("%sr = %s, g = %s, b = %s, a = %s,",
		cfgIndent, f("r"), f("g"), f("b"), f("a"))
	lines[#lines+1] = string.format("%smodelfactor = %s, specular = %s, scattering = %s, lensflare = %s,",
		cfgIndent, f("modelfactor"), f("specular"), f("scattering"), f("lensflare"))
	lines[#lines+1] = string.format("%slifetime = %s, sustain = %s, selfshadowing = %s},",
		cfgIndent, fmt(math.floor(lc.lifetime or 0)), f("sustain"), f("selfshadowing"))
	lines[#lines+1] = string.format("%s},", indent)
	return table.concat(lines, "\n")
end

-- Build the entire unit's lights as one `['unitname'] = { ... }` block
-- ready to paste into DeferredLightsGL4config.lua's unitLights table.
local function buildUnitSnippet()
	if not state.currentUnitName or not state.currentUnitDefID then
		return nil, "No unit selected"
	end
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	if not udLights then return nil, "Lights API not available" end
	local lights = udLights[state.currentUnitDefID]
	if not lights then return nil, "No lights for this unit" end

	local names = {}
	for n, lt in pairs(lights) do
		if type(lt) == "table" then names[#names+1] = n end
	end
	table.sort(names)

	local out = {}
	out[#out+1] = string.format("\t['%s'] = {", state.currentUnitName)
	for _, name in ipairs(names) do
		out[#out+1] = snippetForLight(name, lights[name])
	end
	out[#out+1] = "\t},"
	return table.concat(out, "\n")
end

local UNDO_LIMIT = 50

local function snapshotValues()
	local s = {
		lightType = state.lightType,
		pieceName = state.pieceName,
		lightName = state.lightName,
		pointAnim = state.pointAnim,
		values = {},
	}
	for k, v in pairs(state.values) do s.values[k] = v end
	return s
end

-- Captures the full lightParamTable of every currently-selected light so a
-- multi-mode drag can be undone. Only called when multi-mode is active.
local function snapshotMultiSelected()
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	if not udLights or not state.currentUnitDefID then return nil end
	local unitLights = udLights[state.currentUnitDefID]
	if not unitLights then return nil end
	local snap = {}
	for name, _ in pairs(state.selectedLights) do
		local lt = unitLights[name]
		if type(lt) == "table" and lt.lightParamTable then
			local copy = {}
			for i = 1, #lt.lightParamTable do copy[i] = lt.lightParamTable[i] end
			snap[name] = {
				params = copy,
				lightType = lt.lightType,
				pieceIndex = lt.pieceIndex,
			}
		end
	end
	return snap
end

local function pushUndo()
	local snap = snapshotValues()
	-- If multi-mode is active, also snapshot every selected light's full
	-- lightParamTable so a multi-drag undo can put them all back.
	if next(state.selectedLights) and countSelectedLights() > 1 then
		snap.multi = snapshotMultiSelected()
	end
	state.undoStack[#state.undoStack + 1] = snap
	if #state.undoStack > UNDO_LIMIT then
		table.remove(state.undoStack, 1)
	end
end

-- Session-scoped push: snapshots only the FIRST mutation since the user last
-- "settled" (released a mouse button or paused for a moment). Multiple edits
-- in the same drag/keypress session collapse into one undo step.
local sessionDirty = false
local lastEditTime = 0

local function beginEditSession()
	if not sessionDirty then
		pushUndo()
		sessionDirty = true
	end
	lastEditTime = os.clock()
end

local function endEditSession()
	sessionDirty = false
end

-- Called from widget:Update() to auto-end a session after 1.0s of inactivity,
-- so isolated keypress-bumps eventually become separate undo steps.
local function checkEditSessionTimeout()
	if sessionDirty and (os.clock() - lastEditTime) > 1.0 then
		sessionDirty = false
	end
end

local function applySnapshot(snap)
	if not snap then return end
	state.lightType = snap.lightType
	state.pieceName = snap.pieceName or ""
	state.lightName = snap.lightName or "newlight"
	state.pointAnim = snap.pointAnim or "fade"
	for k, _ in pairs(state.values) do state.values[k] = nil end
	for k, v in pairs(snap.values) do state.values[k] = v end

	-- Restore the per-light positions captured for a multi-mode drag so
	-- those edits actually rewind in the deferred renderer.
	if snap.multi then
		local api = WG['lightsgl4']
		local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
		local unitLights = udLights and state.currentUnitDefID and udLights[state.currentUnitDefID]
		if unitLights and api and api.AddLight then
			for name, entry in pairs(snap.multi) do
				local lt = unitLights[name]
				if type(lt) == "table" and lt.lightParamTable then
					for i = 1, #entry.params do lt.lightParamTable[i] = entry.params[i] end
					local vbo = getVBOForType(entry.lightType or lt.lightType or "point")
					if vbo and state.currentUnitID then
						api.AddLight(tostring(state.currentUnitID) .. name,
							state.currentUnitID, entry.pieceIndex or lt.pieceIndex or 0,
							vbo, lt.lightParamTable)
					end
				end
			end
		end
	end
end

