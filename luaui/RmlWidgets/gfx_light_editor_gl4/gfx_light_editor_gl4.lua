local function processPendingConfigReload()
	if not state.pendingConfigReload then return end
	-- Wait until the deferred rendering widget has been re-initialized,
	-- which we detect by GetUnitDefLights() returning a non-empty table again.
	local api = WG['lightsgl4']
	if not api or not api.GetUnitDefLights then return end
	local udLights = api.GetUnitDefLights()
	if not udLights or not next(udLights) then return end
	-- It's back; refresh our editor state.
	state.pendingConfigReload = nil
	rebuildExistingDropdown()
	rebuildPieceDropdown()
	if state.editingExisting then
		loadExistingLight(state.editingExisting)
	else
		applyPreview()
	end
	setStatus("Lights config reloaded")
end

function widget:Update()
	checkEditSessionTimeout()
	processPendingConfigReload()
	-- Tooltip delay: only show after ~475ms of continuous hover.
	if state.tooltipPending and not state.tooltip then
		if (os.clock() - (state.tooltipPendingSince or 0)) >= 0.475 then
			state.tooltip = state.tooltipPending
		end
	end
	updateTooltipElement()

	-- Titlebar press: distinguish click (toggle) from drag (move panel).
	if state.titlebarPress then
		local mxw, myw, lmb = Spring.GetMouseState()
		local p = state.titlebarPress
		if not p.firstUpdateDone then
			-- The very first Update tick after mousedown: lmb might still be
			-- false because the event arrived before Spring's input poll.
			-- Skip this frame to avoid an immediate spurious "release".
			p.firstUpdateDone = true
			spEcho("[LightEditor] titlebarPress first tick lmb="..tostring(lmb))
			return
		end
		if not lmb then
			spEcho("[LightEditor] titlebarPress release moved="..tostring(p.moved))
			-- Released: if we never moved past threshold, treat as click → toggle.
			if not p.moved then
				setOpen(not state.open)
			end
			state.titlebarPress = nil
		else
			local dx = mxw - p.startMx
			local dy = myw - p.startMy
			-- Movement threshold to disambiguate click from drag.
			if not p.moved and (dx*dx + dy*dy) > 16 then
				p.moved = true
			end
			if p.moved then
				local newLeft = p.startLeft + dx
				local newTop  = p.startTop  - dy  -- Spring Y is bottom-up
				local vsx, vsy = Spring.GetViewGeometry()
				if newLeft < 0 then newLeft = 0 end
				if newTop  < 0 then newTop  = 0 end
				if vsx and newLeft > vsx - 50 then newLeft = vsx - 50 end
				if vsy and newTop  > vsy - 50 then newTop  = vsy - 50 end
				state.panelLeft = newLeft
				state.panelTop  = newTop
				local panel = state.document and state.document:GetElementById("le-panel")
				if panel and panel.SetProperty then
					panel:SetProperty("left", newLeft.."dp")
					panel:SetProperty("top", newTop.."dp")
				end
			end
		end
	end

	-- Panel resize: drag the bottom-right handle to grow/shrink the panel.
	if state.panelResize then
		local mxw, myw, lmb = Spring.GetMouseState()
		if lmb then
			local dx = mxw - state.panelResize.startMx
			local dy = myw - state.panelResize.startMy
			-- Mouse Y is bottom-up; growing means dragging DOWN (= negative dy)
			local newW = state.panelResize.startW + dx
			local newH = state.panelResize.startH - dy
			if newW < 200 then newW = 200 end
			if newH < 100 then newH = 100 end
			state.panelWidth = newW
			state.panelHeight = newH
			local panel = state.document and state.document:GetElementById("le-panel")
			if panel and panel.SetProperty then
				panel:SetProperty("width", newW.."px")
				panel:SetProperty("height", newH.."px")
			end
		else
			state.panelResize = nil
		end
	end

	-- Panel drag: if a drag was started by clicking the titlebar, follow the
	-- mouse until the button is released. The panel's left/top inline style
	-- is updated each frame.
	if state.panelDrag then
		local mxw, myw, lmb = Spring.GetMouseState()
		if lmb then
			local dx = mxw - state.panelDrag.startMx
			local dy = myw - state.panelDrag.startMy
			-- Mouse Y in Spring is bottom-up; CSS top is top-down. So invert dy.
			local newLeft = state.panelDrag.startLeft + dx
			local newTop  = state.panelDrag.startTop  - dy
			-- Clamp loosely to viewport so the panel doesn't get lost.
			local vsx, vsy = Spring.GetViewGeometry()
			if newLeft < 0 then newLeft = 0 end
			if newTop  < 0 then newTop  = 0 end
			if vsx and newLeft > vsx - 50 then newLeft = vsx - 50 end
			if vsy and newTop  > vsy - 50 then newTop  = vsy - 50 end
			state.panelLeft = newLeft
			state.panelTop  = newTop
			local panel = state.document and state.document:GetElementById("le-panel")
			if panel and panel.SetProperty then
				panel:SetProperty("left", newLeft.."dp")
				panel:SetProperty("top", newTop.."dp")
			end
		else
			state.panelDrag = nil
		end
	end

	-- Only check hover when not currently dragging (drag locks the highlight)
	if state.dragging then
		state.hoveredLightMarker = nil
		return
	end
	local mx, my, lmb = Spring.GetMouseState()
	state.gizmo = hitTestGizmo(mx, my)
	-- Don't hover-test light markers if a gizmo is already taking precedence.
	if state.gizmo then
		state.hoveredLightMarker = nil
	else
		state.hoveredLightMarker = hitTestLightMarker(mx, my)
	end
	-- For sliders, RmlUi may not fire our widget:MouseRelease. Detect button
	-- release via Spring.GetMouseState as a fallback to close the edit session
	-- right after the mouse comes up.
	if not lmb and sessionDirty and (os.clock() - lastEditTime) > 0.15 then
		sessionDirty = false
	end
end

local function startDrag(mx, my, hit)
	-- Multi-mode drag: snapshot start positions for ALL selected lights.
	if hit.point == "multi" then
		local api = WG['lightsgl4']
		local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
		if not udLights or not state.currentUnitDefID then return end
		local unitLights = udLights[state.currentUnitDefID]
		if not unitLights then return end
		local multiStart = {}
		for name, _ in pairs(state.selectedLights) do
			local lt = unitLights[name]
			if type(lt) == "table" and lt.lightParamTable then
				local p = lt.lightParamTable
				multiStart[name] = {
					posx = p[1] or 0, posy = p[2] or 0, posz = p[3] or 0,
					-- pos2 only relevant for beam lights
					pos2x = (lt.lightType == "beam") and (p[5] or 0) or nil,
					pos2y = (lt.lightType == "beam") and (p[6] or 0) or nil,
					pos2z = (lt.lightType == "beam") and (p[7] or 0) or nil,
					lightType = lt.lightType,
				}
			end
		end
		local cx, cy, cz = getMultiCentroid()
		state.dragging = {
			point = "multi",
			axis = hit.axis,
			startMx = mx,
			startMy = my,
			multiStart = multiStart,
			originWorld = {cx, cy, cz},
		}
		return
	end

	-- Single-edit drag (existing logic).
	local v = state.values
	local startVals = {}
	if hit.point == "pos" or hit.point == "both" then
		startVals.posx = v.posx or 0
		startVals.posy = v.posy or 0
		startVals.posz = v.posz or 0
	end
	if hit.point == "pos2" or hit.point == "both" then
		startVals.pos2x = v.pos2x or 0
		startVals.pos2y = v.pos2y or 0
		startVals.pos2z = v.pos2z or 0
	end

	local axes = getAxisWorldDirs()
	local pointWorldX, pointWorldY, pointWorldZ
	if hit.point == "both" then
		local p1x, p1y, p1z = getPointWorld("pos")
		local p2x, p2y, p2z = getPointWorld("pos2")
		pointWorldX = (p1x + p2x) * 0.5
		pointWorldY = (p1y + p2y) * 0.5
		pointWorldZ = (p1z + p2z) * 0.5
	else
		pointWorldX, pointWorldY, pointWorldZ = getPointWorld(hit.point)
	end

	state.dragging = {
		point = hit.point,
		axis = hit.axis,
		startMx = mx,
		startMy = my,
		startVals = startVals,
		originWorld = {pointWorldX, pointWorldY, pointWorldZ},
	}
end

local function applyDrag(mx, my)
	local d = state.dragging
	if not d then return end
	local axes = getAxisWorldDirs()

	local dxScreen = mx - d.startMx
	local dyScreen = my - d.startMy

	-- For each axis being dragged, compute a world-delta along that axis.
	-- Method: project the mouse-screen delta onto the screen-space direction
	-- of that axis at the gizmo's origin, scaled by world-units-per-pixel.
	local function axisWorldDelta(ax)
		local dirW = axes[ax]
		local ox, oy, oz = d.originWorld[1], d.originWorld[2], d.originWorld[3]
		-- Two world points along the axis, 1 elmo apart, projected to screen
		local s0x, s0y = spWorldToScreenCoords(ox, oy, oz)
		local s1x, s1y = spWorldToScreenCoords(ox + dirW[1], oy + dirW[2], oz + dirW[3])
		if not s0x or not s1x then return 0 end
		local axScreenX = s1x - s0x
		local axScreenY = s1y - s0y
		local axLen2 = axScreenX*axScreenX + axScreenY*axScreenY
		if axLen2 < 1e-6 then return 0 end
		-- mouse_delta projected onto axis_screen_dir, in pixels
		local proj = (dxScreen * axScreenX + dyScreen * axScreenY) / axLen2
		-- proj * 1 elmo per 1-elmo-of-screen-axis = world units along the axis
		return proj
	end

	-- Multi-mode drag: apply same piece-local delta to all selected lights.
	if d.point == "multi" then
		local api = WG['lightsgl4']
		local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
		if not udLights or not state.currentUnitDefID then return end
		local unitLights = udLights[state.currentUnitDefID]

		-- Compute the piece-local delta from this drag.
		local deltas = {x = 0, y = 0, z = 0}
		if d.axis == "x" or d.axis == "y" or d.axis == "z" then
			deltas[d.axis] = axisWorldDelta(d.axis)
		elseif d.axis == "xy" or d.axis == "yz" or d.axis == "xz" then
			local a, b = PLANE_AXES[d.axis][1], PLANE_AXES[d.axis][2]
			deltas[a] = axisWorldDelta(a)
			deltas[b] = axisWorldDelta(b)
		end

		for name, start in pairs(d.multiStart) do
			local lt = unitLights and unitLights[name]
			if type(lt) == "table" and lt.lightParamTable then
				lt.lightParamTable[1] = start.posx + deltas.x
				lt.lightParamTable[2] = start.posy + deltas.y
				lt.lightParamTable[3] = start.posz + deltas.z
				if start.lightType == "beam" and start.pos2x then
					lt.lightParamTable[5] = start.pos2x + deltas.x
					lt.lightParamTable[6] = start.pos2y + deltas.y
					lt.lightParamTable[7] = start.pos2z + deltas.z
				end
				local vbo = getVBOForType(start.lightType or "point")
				if vbo and api.AddLight then
					local ok, err = pcall(api.AddLight,
						tostring(state.currentUnitID) .. name,
						state.currentUnitID, lt.pieceIndex or 0, vbo, lt.lightParamTable)
					if not ok then
						spEcho("[LightEditor] multi AddLight failed for "..name..": "..tostring(err))
					end
				end
			end
		end
		return
	end

	-- Field sets: for "both", apply deltas to BOTH pos and pos2.
	local fieldSets
	if d.point == "both" then
		fieldSets = {POINT_FIELDS.pos, POINT_FIELDS.pos2}
	else
		fieldSets = {POINT_FIELDS[d.point]}
	end

	if d.axis == "all" then
		-- Free 3D drag (center cube): project on all 3 axes, apply to all sets.
		local dx = axisWorldDelta("x")
		local dy = axisWorldDelta("y")
		local dz = axisWorldDelta("z")
		for _, fields in ipairs(fieldSets) do
			setFieldValue(fields[1], d.startVals[fields[1]] + dx)
			setFieldValue(fields[2], d.startVals[fields[2]] + dy)
			setFieldValue(fields[3], d.startVals[fields[3]] + dz)
		end
	elseif d.axis == "xy" or d.axis == "yz" or d.axis == "xz" then
		-- Plane drag: two axes simultaneously.
		local a, b = PLANE_AXES[d.axis][1], PLANE_AXES[d.axis][2]
		local delta_a = axisWorldDelta(a)
		local delta_b = axisWorldDelta(b)
		for _, fields in ipairs(fieldSets) do
			local axisFields = {x=fields[1], y=fields[2], z=fields[3]}
			setFieldValue(axisFields[a], d.startVals[axisFields[a]] + delta_a)
			setFieldValue(axisFields[b], d.startVals[axisFields[b]] + delta_b)
		end
	else
		-- Single-axis drag.
		local delta = axisWorldDelta(d.axis)
		local idx = (d.axis == "x") and 1 or (d.axis == "y") and 2 or 3
		for _, fields in ipairs(fieldSets) do
			local field = fields[idx]
			setFieldValue(field, d.startVals[field] + delta)
		end
	end

	applyPreview()
end

-- Build a unique light-name based on a base, by suffixing _copy / _copy2 / etc.
local function uniqueLightName(unitDefID, baseName)
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	local lights = udLights and udLights[unitDefID]
	if not lights then return baseName.."_copy" end
	local candidate = baseName.."_copy"
	local i = 1
	while lights[candidate] do
		i = i + 1
		candidate = baseName.."_copy"..i
	end
	return candidate
end

-- Duplicate a single light by name. Inserts a deep-copy with a unique name
-- into the runtime unitDefLights and registers it in the VBO so it renders
-- immediately. Returns the new name on success.
local function duplicateLight(unitID, unitDefID, name)
	local api = WG['lightsgl4']
	local udLights = api and api.GetUnitDefLights and api.GetUnitDefLights()
	local lights = udLights and udLights[unitDefID]
	if not lights or type(lights[name]) ~= "table" then return nil end
	local src = lights[name]
	local copyName = uniqueLightName(unitDefID, name)
	-- Deep-copy the lightParamTable and other relevant fields.
	local newParams = {}
	if src.lightParamTable then
		for i = 1, #src.lightParamTable do newParams[i] = src.lightParamTable[i] end
	end
	local newLt = {
		lightType = src.lightType,
		pieceName = src.pieceName,
		pieceIndex = src.pieceIndex,
		lightParamTable = newParams,
		initComplete = true,
	}
	lights[copyName] = newLt
	-- Spawn the copy in the VBO with its own instance ID so it renders.
	local vbo = getVBOForType(newLt.lightType or "point")
	if vbo and api.AddLight and unitID then
		api.AddLight(tostring(unitID) .. copyName, unitID, newLt.pieceIndex or 0, vbo, newLt.lightParamTable)
	end
	return copyName
end

function widget:MousePress(mx, my, button)
	if button ~= 1 then return false end
	-- Gizmo drag has highest priority.
	if state.gizmo then
		-- Alt-drag on a gizmo = duplicate the light(s) first, then drag the copy.
		local alt = Spring.GetModKeyState()
		if alt and state.currentUnitID and state.currentUnitDefID then
			if state.gizmo.point == "multi" then
				-- Duplicate every selected light, swap selection to the copies.
				local newSelected = {}
				for selName, _ in pairs(state.selectedLights) do
					local copy = duplicateLight(state.currentUnitID, state.currentUnitDefID, selName)
					if copy then newSelected[copy] = true end
				end
				if next(newSelected) then
					state.selectedLights = newSelected
					rebuildExistingDropdown()
					setStatus("Duplicated "..countSelectedLights().." lights")
				end
			elseif state.editingExisting then
				local copy = duplicateLight(state.currentUnitID, state.currentUnitDefID, state.editingExisting)
				if copy then
					rebuildExistingDropdown()
					loadExistingLight(copy)
					setStatus("Duplicated to '"..copy.."'")
				end
			end
		end
		beginEditSession()  -- snapshot before first mutation; release closes session
		startDrag(mx, my, state.gizmo)
		return true  -- consume so camera doesn't pan
	end
	-- Click on a light overview marker
	if state.hoveredLightMarker then
		local _, ctrl, _, shift = Spring.GetModKeyState()
		local name = state.hoveredLightMarker
		if ctrl or shift then
			-- Toggle in selection set (multi-select)
			if state.selectedLights[name] then
				state.selectedLights[name] = nil
			else
				state.selectedLights[name] = true
			end
			local n = countSelectedLights()
			if n == 1 then
				for k, _ in pairs(state.selectedLights) do
					loadExistingLight(k)
				end
			elseif n == 0 then
				state.editingExisting = nil
				clearPreview()
				setStatus("")
			else
				state.editingExisting = nil
				clearPreview()
				local sharedPiece = getSelectedSharedPiece()
				if sharedPiece then
					setStatus(n.." lights selected (piece='"..sharedPiece.."'). Drag gizmo to move all.")
				else
					setStatus(n.." lights selected — different pieces, can't move together.")
				end
			end
		else
			-- Plain click: replace selection with this single light + load it.
			state.selectedLights = { [name] = true }
			loadExistingLight(name)
			if not state.open then setOpen(true) end
		end
		refreshMultiModeUI()
		return true
	end
	-- Click empty space (no marker, no gizmo) clears multi-selection.
	if isMultiMode() then
		state.selectedLights = {}
		setStatus("")
		refreshMultiModeUI()
		return false
	end
	return false
end

function widget:MouseMove(mx, my, dx, dy, button)
	if state.dragging then
		applyDrag(mx, my)
		return true
	end
	return false
end

function widget:MouseRelease(mx, my, button)
	if state.dragging then
		state.dragging = nil
		endEditSession()
		return -1
	end
	-- Slider release also closes the edit session so the next slider drag
	-- starts a fresh undo step.
	endEditSession()
	return false
end

local KEY_Z = 122

function widget:KeyPress(key, mods, isRepeat)
	-- Ctrl+Z anywhere = undo (only while panel is open and we have a unit selected)
	if key == KEY_Z and mods and mods.ctrl and state.open and state.currentUnitID then
		if #state.undoStack == 0 then
			setStatus("Nothing to undo")
		else
			local snap = table.remove(state.undoStack)
			applySnapshot(snap)
			applyAllValuesToUI()
			if state.document then
				local pieceDisp = state.document:GetElementById("le-piece-display")
				if pieceDisp then pieceDisp.inner_rml = (state.pieceName ~= "" and state.pieceName) or "(world)" end
			end
			setLightType(state.lightType)
			applyPreview()
			setStatus("Undo ("..#state.undoStack.." remaining)")
		end
		return true
	end

	if not state.focusedField then return false end
	if key == KEY_UP then
		bumpField(state.focusedField, 1)
		return true
	elseif key == KEY_DOWN then
		bumpField(state.focusedField, -1)
		return true
	end
	-- Block other keys from leaking to camera while a numbox has focus
	-- (lets the user type digits/decimals into the input freely).
	return false
end

