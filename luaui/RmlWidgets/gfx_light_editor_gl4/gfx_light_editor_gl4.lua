function widget:Initialize()
	state.rmlContext = RmlUi.GetContext("shared")
	if not state.rmlContext then
		spEcho("[LightEditor] no shared RmlUi context")
		return false
	end

	for k, v in pairs(defaultLight) do
		state.values[k] = v
	end

	-- All handlers + bound vars MUST be present at OpenDataModel time.
	-- Adding/replacing functions later is forbidden by Recoil's RmlUi binding.
	local initialModel = {
		-- bound text/values (two-way binding via data-value)
		lightName = state.lightName,
		-- per-field slider raw values (scaled ints, see sliderScale)
		sl_posx = 0, sl_posy = 50, sl_posz = 0, sl_radius = 300,
		sl_r = 100, sl_g = 100, sl_b = 100, sl_a = 100,
		sl_color2r = 100, sl_color2g = 100, sl_color2b = 100, sl_colortime = 0,
		sl_dirx = 0, sl_diry = -10, sl_dirz = 100, sl_theta = 50,
		sl_pdirx = 0, sl_pdiry = 0, sl_pdirz = 0, sl_ptheta = 0,
		sl_pos2x = 0, sl_pos2y = 0, sl_pos2z = 500,
		sl_modelfactor = 50, sl_specular = 50, sl_scattering = 50, sl_lensflare = 0,
		sl_lifetime = 0, sl_sustain = 0, sl_selfshadowing = 0,

		-- declarative event handlers (data-event-click="onAction('...')")
		onAction = function(_ev, action, arg)
			if action == "toggle" then
				setOpen(not state.open)
				if state.open then
					local sel = spGetSelectedUnits()
					if sel and sel[1] and state.currentUnitID ~= sel[1] then
						setSelectedUnit(sel[1])
					else
						rebuildPieceDropdown()
						rebuildExistingDropdown()
					end
				end
			elseif action == "close" then
				setOpen(false)
			elseif action == "settype" then
				setLightType(arg)
			elseif action == "copy" then
				local snippet = generateSnippet()
				if spSetClipboard then spSetClipboard(snippet) end
				spEcho("[LightEditor] Snippet copied:\n"..snippet)
				setStatus("Copied to clipboard")
			elseif action == "reset" then
				pushUndo()
				for k, v in pairs(defaultLight) do state.values[k] = v end
				state.lightName = "newlight"
				state.pieceName = ""
				state.editingExisting = nil
				applyAllValuesToUI()
				setLightType("point")
				setStatus("Reset")
			elseif action == "reloadConfig" then
				-- Clear our preview so it doesn't leak across the reload.
				clearPreview()
				-- Toggle the deferred rendering widget twice via SendCommands
				-- (off, then on). This re-runs its Initialize() which re-reads
				-- DeferredLightsGL4config.lua and rebuilds unitDefLights.
				-- Sent as one command string so they queue up in order.
				Spring.SendCommands(
